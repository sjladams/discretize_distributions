import torch
import discretize_distributions as dd

import discretize_distributions.schemes as dd_schemes
import discretize_distributions.distributions as dd_dists
import discretize_distributions.optimal as dd_optimal
import numpy as np
import time
import pandas as pd
import math
from itertools import product
import random
import examples.hyper_parameter_search as hyper
from scipy.optimize import minimize_scalar

torch.manual_seed(0)
results = []
run_id = 0
test_nr = 4

all_pairs = list(product(range(2,10), [2, 4, 5]))
selected_pairs = random.sample(all_pairs, 10)

for run_id, (num_dims, num_mix_elems) in enumerate(selected_pairs, 1):
    print(f"\n--- Run {run_id}: dims={num_dims}, components={num_mix_elems} ---")

    loc = torch.zeros((num_mix_elems, num_dims))  # overlapping
    cov = torch.diag_embed(torch.rand((num_mix_elems, num_dims)))
    component_distribution = dd_dists.MultivariateNormal(loc=loc, covariance_matrix=cov)
    mixture_distribution = torch.distributions.Categorical(probs=torch.rand((num_mix_elems,)))
    gmm = dd_dists.MixtureMultivariateNormal(mixture_distribution, component_distribution)

    start = time.time()
    _, centers, _ = dd_optimal.dbscan_shells(gmm=gmm)
    w2_history = []
    state = {
        'best_w2': float('inf'),
        'best_mix_grid': None
    }
    early_stop_window = 3  # how many values to consider for stopping
    early_stop_tol = 1e-4  # how close values are from each other to stop

    def objective(eps):
        try:
            mix_grid = dd_optimal.create_grid_from_epsilon(gmm, centers, eps)
            disc_mix, w2_mix = dd.discretize(gmm, mix_grid)
            w2_value = w2_mix.item()
            print(f"eps: {eps:.4f}, w2_mix: {w2_value:.6f}")

            # save best values
            if w2_value < state['best_w2']:
                state['best_w2'] = w2_value
                state['best_mix_grid'] = mix_grid

            # history
            w2_history.append(w2_value)

            # early stopping
            if len(w2_history) >= early_stop_window:
                recent = w2_history[-early_stop_window:]
                if max(recent) - min(recent) < early_stop_tol:
                    print("Early stopping: w2_mix stabilized.")
                    raise StopIteration  # stops

            return w2_value

        except Exception as e:
            print(f"Error at eps={eps:.4f}: {e}")
            return 1e6  # failures

    # Golden-section search
    lower = 1*(num_dims)**(1/2)
    upper = 10*(num_dims)**(1/2)
    result = minimize_scalar(objective, bounds=(lower, upper), method='bounded')

    # results
    best_eps = result.x
    best_w2 = state['best_w2']
    best_mix_grid = state['best_mix_grid']
    print(f"\nBest eps: {best_eps:.4f}, with w2_mix: {best_w2:.4f}")

    try:
        # using mix_grids with domain(s) generated by DBSCAN
        mix_grid = dd_optimal.create_grid_from_epsilon(gmm, centers, best_eps)
        disc_mix, w2_mix = dd.discretize(gmm, mix_grid)
        mix_time = time.time() - start

    except Exception as e:
        print(f"Skipping run {run_id} due to error: {e}")
        continue

    # Old method
    # nr_locs = len(disc_mix.locs)
    # rounded_value = round(nr_locs / 10) * 10
    x = int(100 / num_mix_elems)

    start = time.time()
    grid_schemes = []
    for i in range(num_mix_elems):
        grid_schemes.append(dd_optimal.get_optimal_grid_scheme(gmm.component_distribution[i], num_locs=x))
    disc_old, w2_old = dd.discretize_gmms_the_old_way(gmm, grid_schemes)
    old_time = time.time() - start

    # logging
    results.append({
        "run": run_id,
        "num_dims": num_dims,
        "num_mix_elems": num_mix_elems,
        "w2_mix": best_w2,
        "w2_old": w2_old,
        "time_mix": mix_time,
        "time_old": old_time,
        "best_eps": best_eps,
        "nr_locs_mix": len(disc_mix.locs),
        "nr_locs_old": len(disc_old.locs),
    })

df = pd.DataFrame(results)
df.to_csv(f"gmm_discretization_results_test_{test_nr}.csv", index=False)
print(f"Results saved")